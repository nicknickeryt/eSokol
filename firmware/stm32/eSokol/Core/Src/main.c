void jumpSel(int button) {

	if (currPage == 0)
		nextPage = button == 0 ? 1 : 2;

	else if (currPage == 1) { // settings
		if (button == 1) {
			nextPage = 0;
			return;
		} else if (button == 0) {
			switch(nextSel) {
				case 0:
					nextPage = 2;
					break;
				case 2:
					nextAction = 1;
					break;
			}
		}


	} else if (currPage == 2) { // lights
		if (button == 1) {
			nextPage = 0;
			return;
		} else if (button == 0) {

			switch(nextSel) {
				case 0:
					nextAction = 0;
					break;
				case 1:
					nextAction = 2;
					break;
			}
		}
	}

}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
	if (GPIO_Pin == GPIO_PIN_1 && ((HAL_GetTick() - timeMs) > 150)
			&& HAL_GPIO_ReadPin(GPIOB, GPIO_Pin) == 0) // If The INT Source Is EXTI Line9 (A9 Pin)
					{
		jumpSel(0);
		timeMs = HAL_GetTick();
	} else if (GPIO_Pin == GPIO_PIN_0 && ((HAL_GetTick() - timeMs) > 150)
			&& HAL_GPIO_ReadPin(GPIOB, GPIO_Pin) == 0) // If The INT Source Is EXTI Line9 (A9 Pin)
					{
		jumpSel(1);
		timeMs = HAL_GetTick();
	}
}

