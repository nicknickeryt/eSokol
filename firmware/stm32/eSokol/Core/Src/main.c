	voltage = voltage/10;


	uint8_t hour = DS3231_GetHour();
	uint8_t minute = DS3231_GetMinute();

	ssd1306_SetCursor(0, 0);
	ssd1306_WriteString("ROMET", Font_6x8, White);

	ssd1306_SetCursor(51, 0);

	char buf[5];
	formatTime(hour, minute, buf);


